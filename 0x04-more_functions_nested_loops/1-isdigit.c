/**
* _isdigit - check if numbers are 0 - 9
* @c: char to be checked
*
* Return: 0 or 1
*/
#include "main.h"

/**
 * _isdigit - checks for a digit (0 through 9)
 * @c: int to be checked
 * Return: 1 if c is a digit, 0 otherwise
 */
int _isdigit(int c)
{
	return (c >= '0' && c <= '9');
}
